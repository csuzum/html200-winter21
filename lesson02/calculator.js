// Lesson 02-01: 4 variables, using operators: =  +  *  \  -  /  %  ++  --  **

let cheesePizza = 17;
let pepperoni = 3;
let olives = 2;
let coupon = -1.5;

console.log(cheesePizza * 2 + pepperoni + olives * 2);

console.log( ((cheesePizza + pepperoni) * 2) + (cheesePizza + olives + olives / 2) * 3);

console.log((cheesePizza + olives / 2) + coupon);

console.log(cheesePizza % coupon);

console.log((cheesePizza + ++pepperoni) + (cheesePizza + --olives));

console.log(cheesePizza ** 2);
